public class ReverseArray {
    public static void main(String[] args) {
        int[] originalArray = {1, 2, 3, 5, 7};
        System.out.print("Array in reverse order: ");
        for (int i = originalArray.length - 1; i >= 0; i--)
            {

            System.out.print(originalArray[i] + " ");

        }


    }


}
